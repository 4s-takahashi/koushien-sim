/**
 * 能力名とランクから言葉化テキストを返す。
 *
 * 同じ能力・ランクでは常に同じ候補から決定論的に選ぶ
 * （シード不要・純粋関数）。
 * 候補が複数ある場合は 0 番目（1番目）を返す（現実装では固定）。
 *
 * @param ability  能力名（StatRowView.label と一致）
 * @param rank     AbilityRank ('S'〜'E'、またはプロジェクターの変換後)
 * @param index    候補インデックス（default: 0）
 */
export function getAbilityNarrative(
  ability: AbilityKey,
  rank: AbilityRank,
  index: number = 0,
): string {
  const pool = NARRATIVES[ability];
  if (!pool) return '';
  const candidates = pool[rank] ?? [];
  if (candidates.length === 0) return '';
  return candidates[index % candidates.length];
}

/**
 * 能力名とランクから全候補リストを返す（テスト・デバッグ用）。
 */
export function getAbilityNarrativeCandidates(
  ability: AbilityKey,
  rank: AbilityRank,
): string[] {
  return NARRATIVES[ability]?.[rank] ?? [];
}

